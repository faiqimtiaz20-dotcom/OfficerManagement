-- =========================
-- Identity and Access
-- =========================
-- Global identity: one account per person (email unique platform-wide).
-- Login with verified email OR optional username + password_hash.
-- Tenancy is via tenant_user (same user may belong to multiple tenants).
CREATE TABLE app_user (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  full_name text NOT NULL,
  email text UNIQUE NOT NULL,
  username text,
  password_hash text NOT NULL,
  password_updated_at timestamptz NOT NULL DEFAULT now(),
  last_login_at timestamptz,
  phone text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT app_user_username_trim CHECK (username IS NULL OR length(trim(username)) > 0)
);

CREATE UNIQUE INDEX ux_app_user_username_lower ON app_user (lower(username))
  WHERE username IS NOT NULL;

CREATE INDEX idx_app_user_email_lower ON app_user (lower(email));

-- =========================
-- Billing / subscription (SaaS)
-- =========================
CREATE TABLE subscription_plan (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text UNIQUE NOT NULL,
  name text NOT NULL,
  description text,
  billing_interval text NOT NULL CHECK (billing_interval IN ('monthly', 'yearly')),
  price_amount numeric(12, 2) NOT NULL,
  currency char(3) NOT NULL DEFAULT 'GBP',
  max_users int,
  max_sites int,
  max_officers int,
  features jsonb,
  is_public boolean NOT NULL DEFAULT true,
  sort_order smallint NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_subscription_plan_public ON subscription_plan (is_public, sort_order);

-- Platform-defined modules / entitlements (linked to plans via subscription_plan_feature)
CREATE TABLE subscription_feature (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text UNIQUE NOT NULL,
  name text NOT NULL,
  description text,
  sort_order smallint NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_subscription_feature_sort ON subscription_feature (sort_order);

CREATE TABLE subscription_plan_feature (
  subscription_plan_id integer NOT NULL REFERENCES subscription_plan(id) ON DELETE CASCADE,
  subscription_feature_id integer NOT NULL REFERENCES subscription_feature(id) ON DELETE CASCADE,
  PRIMARY KEY (subscription_plan_id, subscription_feature_id)
);

CREATE INDEX idx_subscription_plan_feature_feature ON subscription_plan_feature (subscription_feature_id);

CREATE TABLE tenant (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  slug text UNIQUE NOT NULL,
  status text NOT NULL CHECK (status IN ('active','inactive')),
  subscription_plan_id integer REFERENCES subscription_plan(id) ON DELETE SET NULL,
  subscription_status text NOT NULL DEFAULT 'none' CHECK (
    subscription_status IN ('none', 'trialing', 'active', 'past_due', 'cancelled')
  ),
  subscription_started_at timestamptz,
  subscription_current_period_end timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT tenant_subscription_plan_consistent CHECK (
    subscription_status = 'none' OR subscription_plan_id IS NOT NULL
  )
);

CREATE INDEX idx_tenant_subscription_plan ON tenant (subscription_plan_id);

CREATE TABLE role (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text UNIQUE NOT NULL,
  name text NOT NULL
);

CREATE TABLE tenant_user (
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  user_id integer NOT NULL REFERENCES app_user(id) ON DELETE CASCADE,
  is_owner boolean NOT NULL DEFAULT false,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (tenant_id, user_id)
);

CREATE TABLE tenant_user_role (
  tenant_id integer NOT NULL,
  user_id integer NOT NULL,
  role_id integer NOT NULL REFERENCES role(id) ON DELETE RESTRICT,
  PRIMARY KEY (tenant_id, user_id, role_id),
  FOREIGN KEY (tenant_id, user_id) REFERENCES tenant_user(tenant_id, user_id) ON DELETE CASCADE
);

CREATE TABLE branch (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
