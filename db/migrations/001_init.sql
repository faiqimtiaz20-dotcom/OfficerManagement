-- =========================
-- Scheduling, Calls, Reconciliation
-- =========================
CREATE TABLE shift (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  client_id integer NOT NULL REFERENCES client(id),
  site_id integer NOT NULL REFERENCES site(id),
  duty_type_id integer REFERENCES duty_type(id),
  branch_id integer REFERENCES branch(id),
  officer_id integer REFERENCES officer(id),
  shift_date date NOT NULL,
  start_time time NOT NULL,
  end_time time NOT NULL,
  break_minutes integer NOT NULL DEFAULT 0,
  planned_hours numeric(6,2),
  charge_rate numeric(10,2),
  pay_rate numeric(10,2),
  currency_code char(3) NOT NULL DEFAULT 'GBP',
  status text NOT NULL CHECK (status IN ('confirmed','pending','unassigned','cancelled')),
  created_by integer REFERENCES app_user(id),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE shift_call_plan (
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  shift_id integer PRIMARY KEY REFERENCES shift(id) ON DELETE CASCADE,
  precheck_due_at timestamptz,
  bookon_window_start timestamptz,
  bookon_window_end timestamptz,
  check_call_enabled boolean NOT NULL DEFAULT false,
  check_call_interval_minutes integer
);

CREATE TABLE shift_check_call_window (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  shift_id integer NOT NULL REFERENCES shift(id) ON DELETE CASCADE,
  window_start timestamptz NOT NULL,
  window_end timestamptz NOT NULL,
  sequence_no integer NOT NULL
);

CREATE TABLE shift_call_log (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  shift_id integer NOT NULL REFERENCES shift(id) ON DELETE CASCADE,
  call_type text NOT NULL CHECK (call_type IN ('precheck','bookon','checkcall')),
  check_window_id integer REFERENCES shift_check_call_window(id),
  attempted_at timestamptz NOT NULL DEFAULT now(),
  outcome text NOT NULL,
  notes text,
  logged_by integer REFERENCES app_user(id)
);

CREATE TABLE shift_timesheet (
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  shift_id integer PRIMARY KEY REFERENCES shift(id) ON DELETE CASCADE,
  actual_start timestamptz,
  actual_end timestamptz,
  worked_hours numeric(6,2),
  attendance_status text CHECK (attendance_status IN ('on-time','late','no-show')),
  reconciled boolean NOT NULL DEFAULT false,
  reconciled_at timestamptz,
  reconciled_by integer REFERENCES app_user(id)
);

