CREATE TABLE invoice (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id integer NOT NULL REFERENCES tenant(id) ON DELETE CASCADE,
  invoice_number text NOT NULL,
